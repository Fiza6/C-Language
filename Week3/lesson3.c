#include <stdio.h>

int main() {

    int a, b;
    char op; 

    switch (op)
    {
    case '+':
        /* code */
        printf("The sum is : " , a+b);
        break;
    
    case "-" :
        printf("The sum is : " , a-b);
    
    default:
        break;
    }
    

    return 0;
}